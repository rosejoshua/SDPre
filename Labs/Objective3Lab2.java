public class Objective3Lab2 {
  public static void main(String[] args) {
    double side1, side2, hypot;

    side1 = 4.3;
    side2 = 9.34;
    hypot = Math.sqrt(side1 * side1 + side2 * side2);

    System.out.println("The hypotenuse of a triangle with sides " + side1 +
    " and " + side2 + " is " + hypot + ".");

  }
}
